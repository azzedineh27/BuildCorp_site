import i18n from "i18next";
import { initReactI18next } from "react-i18next";
import LanguageDetector from "i18next-browser-languagedetector"; // ðŸ‘ˆ important

import translationFR from "./locales/fr.json";
import translationEN from "./locales/en.json";
import translationES from "./locales/es.json";

const resources = {
  fr: { translation: translationFR },
  en: { translation: translationEN },
  es: { translation: translationES }
};

i18n
  .use(LanguageDetector) // ðŸ‘ˆ ajouter ici
  .use(initReactI18next)
  .init({
    resources,
    fallbackLng: "fr",
    interpolation: {
      escapeValue: false
    },
    detection: {
      order: ['localStorage', 'navigator'], // ordre des sources
      caches: ['localStorage'] // oÃ¹ sauvegarder la langue
    }
  });

export default i18n;
